int main()
{
	Window window(600, 600, true, true);

	ShaderProgram shaderProgram;
	Renderer renderer;
	Camera camera(window.windowWidth, window.windowWidth, glm::vec3(0.0f, 0.0f, 2.0f));

	VertexArray VAO;
	VertexBuffer VBO(vertices, sizeof(vertices), GL_STATIC_DRAW);
	VertexBufferLayout VBL;
	VBL.push<float>(3, false);
	VBL.push<float>(2, false);
	VAO.addBuffer(VBO, VBL);

	Texture texture("test.png");
	texture.Bind(0);
	shaderProgram.setInt("Texture", 0);

	double prevTime = 0.0;
	double currTime = 0.0;
	double timeDiff;
	unsigned int counter = 0;

	while (!glfwWindowShouldClose(window.getWindow()))
	{
		window.displayFrameRate();
		camera.matrix(45.0f, 0.1f, 100.0f, shaderProgram, "camera");
		camera.inputs(window);

		glm::mat4 model = glm::mat4(1.0f);
		model = glm::translate(model, glm::vec3(0.0f, 0.0f, 0.0f));
		model = glm::rotate(model, (float)glfwGetTime() * glm::radians(50.0f), glm::vec3(0.5f, 1.0f, 0.0f));

		shaderProgram.setMat4("model", model);

		renderer.clear();

		renderer.draw(VAO, shaderProgram, (sizeof(vertices) / 4) / 5);

		glfwSwapInterval(0);
		glfwSwapBuffers(window.getWindow());
		glfwPollEvents();
	}

	glfwTerminate();
	return 0;
}
